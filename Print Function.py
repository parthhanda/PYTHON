# Print Function is used to print anything in python.
print("Hello World")
#strings/words can only be prined while in double quotes only("").
# Anything written in double quotes("") wil be printed a it is.
print("34+35")
# Operators can be directly used in the brackets of print to do the required calculations.
print(34+35)
# A comma(,) can be used to write in the single print command,which will be displayed along with the previous words.
print("Hello World","of python",22)
# Print function can also be used for comparison and the output will be in True/False
print(34<35)
print(34>35)