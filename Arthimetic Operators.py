# Arithmetic operators are used with numeric values to perform common mathematical operations.
a=10
b=20

# +	Addition
print(a+b) #30

# -	Subtraction
print(a-b) #-10

# *	Multiplication
print(a*b) #200

# /	Division
print(a/b) #0.5

# %	Modulus (gives the remainder)
print(10%3) #1

# **	Exponentiation (takes the power of a number)
print(5**2) #25

# //	Floor division ()
print(10//3) #3
